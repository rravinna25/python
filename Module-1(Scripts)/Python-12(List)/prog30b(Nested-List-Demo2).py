a=[ [10 , 20] , [30 , 40 , 50] , [60 , 70 , 80 , 90]]  #  Nested list
print(a[0]) # [10 , 20]  :  1st  inner  list  
print(a[1]) #  [30 , 40 , 50] :  2nd  inner  list  
print(a[2]) # [60 , 70 , 80 , 90]  :  3rd inner  list 
print(len(a[0])) #  2 : Number  of  elements  in  1st  inner  list  
print(len(a[1])) #  3 : Number  of  elements  in  2nd  inner  list  
print(len(a[2])) #  4 : Number  of  elements  in  3rd  inner  list  


# Observe  that  each  inner  list  has  got  variable  number  of  elements
