#  sort()  method  demo  program (Home  work)
list = [10 , 20 , 15 , 18 , 5]
print(list) # [10 , 20 , 15 , 18 , 5]
list . sort() #  Sorts  elements  of  list  'a'  replacing  existing  elements
print(list) # [5 , 10 , 15 , 18 , 20]
list . sort(reverse = True) #  Sorts  elements  of  list  'a'  in  descending  order  replacing  existing  elements
print(list) # [20 , 18,  15 , 10,  5]
