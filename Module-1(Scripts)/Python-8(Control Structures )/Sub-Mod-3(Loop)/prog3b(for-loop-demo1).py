for  x  in  [10 , 20 , 15 , 18]:  #  'x'  is  each  element  of  list
	print(x , end = '\t')  #  10  <tab>  20  <tab>  15  <tab>  18  <tab>
print()  #  Moves  to  next  line
for  ch  in  'Hyd':  #  ch  is  each  character  of   string  'Hyd'
	print(ch , end = '\t')  #  H  <tab>  y <tab>  d  <tab>
print()  #  Moves  to  next  line
for  x  in   range(5):  #  'x'  is  each  element  of  range  object
	print(x , end = '\t')  # 0  <tab>  1  <tab>  2  <tab>  3  <tab>  4  <tab>
print()  #  Moves  to  next  line



