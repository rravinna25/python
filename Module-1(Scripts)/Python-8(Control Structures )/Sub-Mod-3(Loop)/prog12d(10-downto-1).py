'''
Write  a  program  to  print  10  ,  9  ,   8  ,  ..... 1

What  is  printed  in  general ?  --->  'i'  where  'i'  varies  from  10  downto  1  in  steps  of  -1

1) Hint:  Do  not  use  range  object

2) Use  while  loop
'''
print('Numbers from 10 downto 1 in steps of -1')
i = 10
while  i >= 1:
	print(i)
	i -=1
