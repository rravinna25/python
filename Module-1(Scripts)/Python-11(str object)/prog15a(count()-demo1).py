#  count()  method  demo  program (Home  work)
a = 'Hyd is green city. Hyd is hitec city. Hyd is his city'
print(a . count('is'))  #  4 :  'is'  is  found   4  times  in  object  'a'  
print(a . count('is' , 19 , 48))  #  3 :   'is'  is  found  3  times  in  object  'a'   between  indexes  19 and  47 
print(a . count('was'))  #  0 :  'was'  is  found  0  times  in  object  'a' 


