# ord()  function  demo  program
print(ord('A'))   #   Returns  unicode  value  of  'A'  i.e.  65
print(ord('Z')) #   Returns  unicode  value  of  'Z'  i.e.  90
print(ord('a')) #   Returns  unicode  value  of  'a'  i.e.  97
print(ord('z'))  #   Returns  unicode  value  of  'z'  i.e.  122
print(ord('0')) #   Returns  unicode  value  of  '0'  i.e.  48
print(ord('9'))  #   Returns  unicode  value  of  '9'  i.e.  57
print(ord('$')) #   Returns  unicode  value  of  '$'  i.e.  36
print(ord(' '))  #   Returns  unicode  value  of  ' '  i.e.  32



'''
ord()  function
------------------
1) What  does  ord()  function  do ?  --->  Converts  character  to  unicode  value

2) What  are  the  unicode  values  of  'A'  -  'Z' ?  --->  65  to  90
    What  are  the  unicode  values  of  'a'  -  'z' ?  --->  97  to  122
    What  are  the  unicode  values  of  '0'  -  '9' ?  ---> 48  to  57

3) What  is  another  name  of  unicode ?  ---> Extended  Ascii

Note:  chr()  and  ord()  are  quite  opposite  functions
'''
