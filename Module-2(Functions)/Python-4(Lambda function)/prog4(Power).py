power = lambda  a = 3.5 , b = 2  :  a ** b  #  Ref  power  points  to  lambda  function
print(power(2 , 3)) #  8 : Executes  lambda  function  with   a = 2 , b = 3  and  result  is  2 ^ 3 
print(power(4.5 , 4)) #  410.0625 : Executes  lambda  function  with  a = 4.5 ,  b = 4  and  result  is  4.5 ^ 4 
print(power()) #  12.25 : Executes  lambda  function  with  a = default  value  3.5 ,  b = default  value  2  and  result  is  3.5 ^ 2 
print(power(9)) #  9 : Executes  lambda  function  with  a = 9  ,  b = default  value  2  and  result  is  9 ^ 2
